import os
import pandas as pd
import numpy as np

# ============================================================
# CONFIGURATION
# ============================================================

INPUT_FILE = "/content/combined_events.csv"
OUTPUT_FILE = "/content/windowed_output/windowed_events.csv"

WINDOW_NS = 100_000_000  # 100 ms

os.makedirs("/content/windowed_output", exist_ok=True)

print("\n🚀 Generating windowed_events.csv in EXACT format\n")


# ============================================================
# LOAD FILE
# ============================================================

df = pd.read_csv(INPUT_FILE)

print("Loaded rows:", len(df))


# ============================================================
# CREATE t_rel IF MISSING
# ============================================================

if "t_rel" not in df.columns:

    df["timestamp"] = (
        df["time_s"].astype(np.int64) * 1_000_000_000 +
        df["time_ns"].astype(np.int64)
    )

    t0 = df["timestamp"].min()

    df["t_rel"] = df["timestamp"] - t0


# ============================================================
# CREATE WINDOW ID
# ============================================================

df["window_id"] = (df["t_rel"] // WINDOW_NS).astype(np.int64)


# ============================================================
# LABEL
# ============================================================

run_label = int(df["label"].iloc[0])


# ============================================================
# MEMORY FEATURES
# ============================================================

mem_df = df[df["source"] == "mem"]

mem_feat = mem_df.groupby("window_id").agg(

    mem_write_count=("source", "count"),
    mem_avg_entropy=("entropy", "mean"),
    mem_std_entropy=("entropy", "std"),
    mem_total_bytes=("size", "sum"),
    mem_gpa_variance=("GPA", "var")

).reset_index()


# ============================================================
# DISK FEATURES
# ============================================================

disk_df = df[df["source"] == "disk"]

disk_feat = disk_df.groupby("window_id").agg(

    disk_write_count=("source", "count"),
    disk_avg_entropy=("entropy", "mean"),
    disk_total_bytes=("size", "sum"),
    disk_lba_variance=("LBA", "var")

).reset_index()


# ============================================================
# MERGE FEATURES
# ============================================================

win_df = pd.merge(
    mem_feat,
    disk_feat,
    on="window_id",
    how="outer"
)


# ============================================================
# FILL MISSING VALUES
# ============================================================

win_df = win_df.fillna(0)


# ============================================================
# ADD TIME FEATURE
# ============================================================

win_df["time_sec"] = win_df["window_id"] * (WINDOW_NS / 1e9)


# ============================================================
# ADD LABEL
# ============================================================

win_df["label"] = run_label


# ============================================================
# EXACT COLUMN ORDER (MATCHES YOUR SCREENSHOT)
# ============================================================

win_df = win_df[[
    "window_id",
    "mem_write_count",
    "mem_avg_entropy",
    "mem_std_entropy",
    "mem_total_bytes",
    "mem_gpa_variance",
    "disk_write_count",
    "disk_avg_entropy",
    "disk_total_bytes",
    "disk_lba_variance",
    "time_sec",
    "label"
]]


# ============================================================
# SORT BY window_id
# ============================================================

win_df = win_df.sort_values("window_id").reset_index(drop=True)


# ============================================================
# SAVE FILE
# ============================================================

win_df.to_csv(
    OUTPUT_FILE,
    index=False
)


# ============================================================
# VERIFY OUTPUT
# ============================================================

print("\n✅ windowed_events.csv generated successfully")
print("Saved at:", OUTPUT_FILE)

print("\nCorrect column order:")
print(list(win_df.columns))

print("\nPreview:")
print(win_df.head())

import numpy as np
