SEQUENCE_LENGTH = 50

OUTPUT_FILE = "/content/X_sequences.npy"


# ============================================================
# FAST SCALER (IDENTICAL TO TRAINING PIPELINE)
# ============================================================

class FastScaler:

    def fit_transform(self, X):

        mean = X.mean(axis=0, dtype=np.float32)

        std = X.std(axis=0, dtype=np.float32)

        std[std == 0] = 1e-8

        return ((X - mean) / std).astype(np.float32)


# ============================================================
# LOAD WINDOWED EVENTS FILE
# ============================================================

print("\nLoading windowed_events.csv...")

df = pd.read_csv(INPUT_FILE)

print("Total windows:", len(df))


# ============================================================
# EXACT FEATURE COLUMN ORDER (CRITICAL — MUST MATCH TRAINING)
# ============================================================

FEATURE_COLUMNS = [

    "mem_write_count",
    "mem_avg_entropy",
    "mem_std_entropy",
    "mem_total_bytes",
    "mem_gpa_variance",

    "disk_write_count",
    "disk_avg_entropy",
    "disk_total_bytes",
    "disk_lba_variance",

    "time_sec"
]


# ============================================================
# VERIFY REQUIRED COLUMNS EXIST
# ============================================================

missing = [col for col in FEATURE_COLUMNS if col not in df.columns]

if len(missing) > 0:

    raise Exception(f"Missing columns in windowed_events.csv: {missing}")


# ============================================================
# EXTRACT FEATURES
# ============================================================

X = df[FEATURE_COLUMNS].to_numpy(dtype=np.float32)

print("Feature matrix shape:", X.shape)


# ============================================================
# SCALE FEATURES (IDENTICAL TO TRAINING)
# ============================================================

scaler = FastScaler()

X_scaled = scaler.fit_transform(X)


# ============================================================
# BUILD SEQUENCES (SLIDING WINDOW)
# ============================================================

print("\nBuilding sequences...")

num_windows = X_scaled.shape[0]

X_sequences = []

for i in tqdm(range(num_windows - SEQUENCE_LENGTH + 1)):

    sequence = X_scaled[i : i + SEQUENCE_LENGTH]

    X_sequences.append(sequence)


# ============================================================
# CONVERT TO NUMPY ARRAY (EXACT FORMAT)
# ============================================================

X_sequences = np.array(X_sequences, dtype=np.float32)


# ============================================================
# VERIFY FORMAT
# ============================================================

print("\n===================================")
print("FORMAT VERIFICATION")
print("===================================")

print("Shape:", X_sequences.shape)
print("Dtype:", X_sequences.dtype)

print("\nExpected format:")
print("(num_sequences, 50, 10)")
print("float32")


# ============================================================
# SAVE FILE
# ============================================================

np.save(OUTPUT_FILE, X_sequences)


print("\n✅ X_sequences.npy GENERATED SUCCESSFULLY")
print("Saved at:", OUTPUT_FILE)

